package com.care.mc.dto;

public class NutInfoDTO {
	private String engName, allergyInfo, originInfo;
	private int weight_G, weight_Ml, calorie, suger, protein, saturated_Fat,
				natrium, caffeine;
	public String getEngName() {
		return engName;
	}
	public void setEngName(String engName) {
		this.engName = engName;
	}
	public String getAllergyInfo() {
		return allergyInfo;
	}
	public void setAllergyInfo(String allergyInfo) {
		this.allergyInfo = allergyInfo;
	}
	public String getOriginInfo() {
		return originInfo;
	}
	public void setOriginInfo(String originInfo) {
		this.originInfo = originInfo;
	}
	public int getWeight_G() {
		return weight_G;
	}
	public void setWeight_G(int weight_G) {
		this.weight_G = weight_G;
	}
	public int getWeight_Ml() {
		return weight_Ml;
	}
	public void setWeight_Ml(int weight_Ml) {
		this.weight_Ml = weight_Ml;
	}
	public int getCalorie() {
		return calorie;
	}
	public void setCalorie(int calorie) {
		this.calorie = calorie;
	}
	public int getSuger() {
		return suger;
	}
	public void setSuger(int suger) {
		this.suger = suger;
	}
	public int getProtein() {
		return protein;
	}
	public void setProtein(int protein) {
		this.protein = protein;
	}
	public int getSaturated_Fat() {
		return saturated_Fat;
	}
	public void setSaturated_Fat(int saturated_Fat) {
		this.saturated_Fat = saturated_Fat;
	}
	public int getNatrium() {
		return natrium;
	}
	public void setNatrium(int natrium) {
		this.natrium = natrium;
	}
	public int getCaffeine() {
		return caffeine;
	}
	public void setCaffeine(int caffeine) {
		this.caffeine = caffeine;
	}
	
}
